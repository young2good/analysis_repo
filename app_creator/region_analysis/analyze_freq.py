def extract_venue(title):
    t = title.replace(" ", "")
    for kw, name in VENUE_KEYWORDS:
        if kw.replace(" ", "") in t:
            return name
    return None


rows = []
for p in posts:
    venue = extract_venue(p["title"])
    sido, sigungu = VENUE_DISTRICT.get(venue, (None, None))
    if sido == "서울":
        region = "서울"
    elif sido == "인천":
        region = "인천"
    elif sigungu:
        region = REGION_GROUP.get(sigungu, "경기 기타")
    else:
        region = None
    rows.append({"board": p["board"], "title": p["title"],
                 "venue": venue, "sido": sido, "sigungu": sigungu, "권역": region})

df = pd.DataFrame(rows)
