ACCESS_SCORE = {"서울": 1, "경기 내권": 2, "인천": 2.5, "경기 외권": 3}


def extract_venue(title):
    t = title.replace(" ", "")
    for kw, name in VENUE_KEYWORDS:
        if kw.replace(" ", "") in t:
            return name
    return None


rows = []
for p in posts:
    venue = extract_venue(p["title"])
    sido, sigungu = VENUE_DISTRICT.get(venue, (None, None))
    if sido == "서울":
        region = "서울"
    elif sido == "인천":
        region = "인천"
    elif sigungu:
        region = REGION_GROUP.get(sigungu, None)
    else:
        region = None
    rows.append({"sigungu": sigungu, "region": region})

df = pd.DataFrame(rows)
df_mapped = df[df["region"].notna()].copy()

# ── 시군구별 게시물수 + 접근성 점수 ────────────────────────────
sigungu_df = (df_mapped.groupby(["region", "sigungu"])
              .size()
              .reset_index(name="count"))
sigungu_df["access_score"] = sigungu_df["region"].map(ACCESS_SCORE)

# 권역별 게시물수 리스트
groups = {r: sigungu_df[sigungu_df["region"] == r]["count"].values
          for r in ["서울", "경기 내권", "경기 외권", "인천"]}

print("=" * 55)
