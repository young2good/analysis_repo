# ── Kruskal-Wallis 검정 ──────────────────────────────────────
print("\n[2] Kruskal-Wallis 검정 (4개 권역)")
h_stat, p_val = stats.kruskal(*groups.values())
print(f"  H = {h_stat:.3f},  p = {p_val:.4f}  "
      f"({'유의 *' if p_val < 0.05 else '비유의'})")

# ── Mann-Whitney 사후검정 (Bonferroni 보정) ──────────────────
region_names = list(groups.keys())
pairs = list(combinations(range(len(region_names)), 2))
alpha_bonf = 0.05 / len(pairs)

print(f"\n[3] Mann-Whitney 사후검정 (Bonferroni α={alpha_bonf:.4f})")
print(f"  {'비교':^26} {'U':>8} {'p':>8} {'유의'})")
print("  " + "-" * 48)
for i, j in pairs:
    a, b = region_names[i], region_names[j]
    u, p = stats.mannwhitneyu(groups[a], groups[b], alternative="two-sided")
    sig = "*" if p < alpha_bonf else ""
    print(f"  {a:<12} vs {b:<12} {u:>6.1f}  {p:>8.4f}  {sig}")

# ── Spearman 상관 ────────────────────────────────────────────
print("\n[4] Spearman 상관 (접근성 점수 vs 게시물수, 시군구 단위)")
rho, p_sp = stats.spearmanr(sigungu_df["access_score"], sigungu_df["count"])
print(f"  rho = {rho:.3f},  p = {p_sp:.4f}  "
      f"({'유의 *' if p_sp < 0.05 else '비유의'})")
print(f"  해석: 접근성 점수가 높을수록(멀수록) 게시물이 {'적음' if rho < 0 else '많음'}")

