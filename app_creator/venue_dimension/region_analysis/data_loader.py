def _assign_zone(row: pd.Series) -> str | None:
    if pd.isna(row["sigungu"]):
        return None
    if row["sido"] == "인천":
        return "경기 인천권"
    return ZONE_MAP.get(row["sigungu"])


def load_data() -> pd.DataFrame:
    path = Path(__file__).parent / "data" / "posts_processed.parquet"
    df = pd.read_parquet(path)
    df["zone"] = df.apply(_assign_zone, axis=1)
    return df
